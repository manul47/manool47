    private void jButton1ActionPerformed() {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        
        try {
            DatabaseClass.con=DriverManager.getConnection("jdbc:mysql://localhost:3306/qoma","root","");
            String query = "SELECT*FROM multiuser WHERE username = ? AND password = ?";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, username);
            ps.setString(2, password);

            DatabaseClass.rs = ps.executeQuery();

            if (DatabaseClass.rs.next()) {
                String jabatan = rs.getString("UserType");
                switchToAppropriateGUI(jabatan);
                dispose();  
            } else {
                JOptionPane.showMessageDialog(this, "Username atau Password salah!");
            }
            DatabaseClass.con.close();
        }catch (NullPointerException|SQLException ex){
            ex.printStackTrace();
        }finally{
            try {
                if(con != null){
                    con.close();
                }
            }catch(NullPointerException|SQLException ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
   
