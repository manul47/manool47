    private void switchToAppropriateGUI(String jabatan) {
        if ("Mahasiswa".equalsIgnoreCase(jabatan)) {
            //sesuaikan dengan nama class pada projek file masing2
            Mahasiswa mhs = new Mahasiswa();
            mhs.setVisible(true);
        } else if ("Dosen".equalsIgnoreCase(jabatan)) {
            Dosen dosen = new Dosen();
            dosen.setVisible(true);
        } else if ("Admin".equalsIgnoreCase(jabatan)) {
            Admin adm = new Admin();
            adm.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "User tidak ditemukan");
        }
    }
    
